    public ArrayList<Sinhvien> sinhvienlist() throws ClassNotFoundException, SQLException {
        ArrayList<Sinhvien> ds = new ArrayList<>();
        try {
            Class.forName(driver);
            Connection con = DriverManager.getConnection(url);
            String query = "select * from SinhVien";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            Sinhvien sinhvien = null;
            while (rs.next()) {
                sinhvien = new Sinhvien(rs.getString("MaSV"), rs.getString("TenSV"), rs.getString("NgaySinh"), rs.getString("Lop"), rs.getFloat("diemjava"), rs.getFloat("diemsql"), rs.getFloat("diemav"), rs.getFloat("diemtb"));
                ds.add(sinhvien);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        return ds;
    }

    public void hienthi() throws ClassNotFoundException, SQLException {
        ArrayList<Sinhvien> list = sinhvienlist();
        DefaultTableModel md = (DefaultTableModel) tbds.getModel();
        Object[] row = new Object[4];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getmasv();
            row[1] = list.get(i).gettensv();
            row[2] = list.get(i).getngaysinh();
            row[3] = list.get(i).getlop();
            md.addRow(row);
        }
    }

    public void status(String query, String message) throws SQLException {
        try {
            Connection con = DriverManager.getConnection(url);
            Statement st = con.createStatement();
            if ((st.executeUpdate(query)) == 1) {
                DefaultTableModel md = (DefaultTableModel) tbds.getModel();
                md.setRowCount(0);
                hienthi();
                JOptionPane.showMessageDialog(null, "Already " + message);
            } else {
                JOptionPane.showMessageDialog(null, "Error " + message);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

