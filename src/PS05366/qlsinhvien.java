   public int batloi() {
        if(txtma.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter a student code");
            txtma.requestFocus();
        }else if (txtma.getText().matches("^PS[0-9]{5}$") == false) {
            JOptionPane.showMessageDialog(this, "Import in PSxxxxx format");
            txtma.requestFocus();
        } else if(txtname.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter a name");
            txtname.requestFocus();
        }else if (txtname.getText().matches("^[a-zA-Z \\àáạãăắằặẵâấầẫẳặêềếệễôỏỗộốồơởỡớờịĩíìuúùụũủưửữừứựđÀÁẠÃĂẮẰẶẴÂẤẦẪẲẶÊẾỀỆỄÔỎỖỘỐỒƠỞỠỚỜỊĨÍÌUÚÙỤŨỦƯỬỮỪỨỰĐ]{2,30}$") == false) {
            JOptionPane.showMessageDialog(this, "Greater than 2 characters, include(a-z)+(A-Z)");
            txtname.requestFocus();
        } else if(txtdate.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter a date of birth");
            txtdate.requestFocus();
        } else if (txtdate.getText().matches("^[0-9]{1,2}/[0-9]{1,2}/[0-9]{2,4}$") ==false) {
            JOptionPane.showMessageDialog(this, "mm/dd/yyyy");
            txtdate.requestFocus();
        } else if(txtclass.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter a class");
            txtclass.requestFocus();
        } else if (txtclass.getText().matches("^PT[0-9]{5}$") ==false) {
            JOptionPane.showMessageDialog(this, "Import in PTxxxxx format");
            txtclass.requestFocus();
        } else {
            return 0;
        }
        return 1;
    }

